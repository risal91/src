//Schreiben Sie ein Programm, das 6 unterschiedliche ganze Zahlen zwischen 0 und 99 abspeichert. (Diese Zahlen dürfen von Ihnen beliebig gewählt werden).
//        Um die folgende Ausgabe besser beschreiben zu können, nehmen wir an, dass z.B. die folgenden Zahlen gewählt wurden: (3 / 11 / 15 / 17 / 33 / 42)
//        dann soll entsprechend dieser Auswahl der folgende 6-zeilige Text ausgegeben werden
//        1.Zahl: 3
//        2.Zahl: 11
//        3.Zahl: 15
//        4.Zahl: 17
//        5.Zahl: 33
//        6.Zahl: 42
//        Das Programm endet anschließend.

public class Aufgabe3 {
    public static void main(String[] args) {

        int zahl1 = 3;
        int zahl2 = 11;
        int zahl3 = 15;
        int zahl4 = 17;
        int zahl5 = 33;
        int zahl6 = 42;

        System.out.println("1.Zahl: " + zahl1);
        System.out.println("2.Zahl: " + zahl2);
        System.out.println("3.Zahl: " + zahl3);
        System.out.println("4.Zahl: " + zahl4);
        System.out.println("5.Zahl: " + zahl5);
        System.out.println("6.Zahl: " + zahl6);


    }
}
